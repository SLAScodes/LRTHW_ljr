types_of_people = 10 #Assigns value of 10 to variable 'types_of_people'
x = "There are #{types_of_people} types of people." #assigns 10 to the variable x
binary = 'binary' #Assigns the string 'binary' to the variable named 'binary'
do_not = "don't" #assigns the string 'don't' to the variable named 'do_not'
y = "Those who know #{binary} and those who #{do_not}." #Assigns to variable 'y' statements

puts x #prints value assigned to variable x
puts y #prints value assigned to variable y

puts "I said: #{x}." #prints value in 'x' after the 'I said' statement
puts "I also said: '#{y}' ." #prints value in 'y' after the 'I also said...' statement

hilarious = true #states that the value in the word 'hilarious' is false
joke_evaluation = "Isn't that joke so funny?! #{hilarious}" #prints statement along with variable 'hilarious'

puts joke_evaluation #prints the value of variable 'joke_evaluation'

w = "This is the left side of..." #prints string assigned to variable w
e = "a string with a right side." #prints string assigned to variable e

puts w + e #prints the value of variable 'w' + the value in variable 'e'